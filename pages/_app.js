function UserStateWrapper({
  Component, pageProps, router,
}) {
  const [user, forceUpdate] = useAuthed(false);
  return (
    <>
      <Header user={user} updateUser={forceUpdate} />
      <Component {...pageProps} user={user} updateUser={forceUpdate} router={router} />
    </>
  );
}

export default function MyApp(props) {
  const { Component, pageProps, router } = props;

  React.useEffect(() => {
    // Remove the server-side injected CSS.
    const jssStyles = document.querySelector('#jss-server-side');
    if (jssStyles) {
      jssStyles.parentElement.removeChild(jssStyles);
    }
  }, []);

  return (
      <React.Fragment>
        <Head>
          <title>{title}</title>
          {/* Progressive Web App: Match the width of appâ€™s content with width of viewport for mobile devices */}
          <meta name="viewport" content="minimum-scale=1, initial-scale=1, width=device-width" />
        </Head>
        <ThemeProvider theme={theme}>
          <CssBaseline />
          <SnackbarProvider SnackbarProps={{ autoHideDuration: 4000 }} SnackbarComponent={CustomSnackbar}>
            <UserStateWrapper Component={Component} pageProps={pageProps} router={router} />
          </SnackbarProvider>
        </ThemeProvider>
      </React.Fragment>
  );
}

MyApp.componentDidMount = function() {
  const handleRouteChange = (url) => GTMPageView(url);
  Router.events.on('routeChangeComplete', handleRouteChange);
  // Router.events.off('routeChangeComplete', handleRouteChange);
};
